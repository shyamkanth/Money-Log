package io.github.shyamkanth.moneylog.utils

import android.util.Log

object Logger {
    fun log(message: String?){
        if(message != null) {
            Log.d("Money Log Logger",
                "----------------------------------------------------------------------------------------------------------\n" +
                        "|\n" +
                        "|\n" +
                        "| $message \n" +
                        "|\n" +
                        "|\n" +
                        "----------------------------------------------------------------------------------------------------------")
        } else {
            Log.d("Money Log Logger",
                "----------------------------------------------------------------------------------------------------------\n" +
                        "|\n" +
                        "|\n" +
                        "| NULLLLLLLLLLLLLLLLLLLLLLL \n" +
                        "|\n" +
                        "|\n" +
                        "----------------------------------------------------------------------------------------------------------")
        }
    }
}